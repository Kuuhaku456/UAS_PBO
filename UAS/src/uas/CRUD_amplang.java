    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        int baris = tabel_amplang.getSelectedRow();
        int option = JOptionPane.showConfirmDialog(null, "Apakah Anda yakin ingin menyimpan perubahan ini ???", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (option == JOptionPane.YES_OPTION) {
            Connection conn = Koneksi.getConnection();
            String hargatext = this.txt_harga.getText();
            int harga = Integer.parseInt(hargatext);
            String levelPedas = combo_level.getSelectedItem().toString();
            String sql = "UPDATE tb_amplang SET nama=?, harga=?, jenis=?, level=?, foto=? WHERE id=?";
            try {
                PreparedStatement stmt = conn.prepareStatement(sql);
                stmt.setString(1, this.txt_nama.getText());
                stmt.setInt(2, harga);
                if(this.rb_amplang_ayam.isSelected()){
                    String jenis = "Amplang ayam";
                    stmt.setString(3, jenis);
                } else if (this.rb_amplang_daging.isSelected()){
                    String jenis = "Amplang daging";
                    stmt.setString(3, jenis);
                } else if (this.rb_amplang_ikan.isSelected()){
                    String jenis = "Amplang ikan";
                    stmt.setString(3, jenis);
                }
                stmt.setString(4, levelPedas);
                stmt.setString(5, this.path_txt.getText());
                stmt.setString(6, tabel_amplang.getValueAt(baris, 0).toString());
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data Successfully Updated!!! ", "SQL Information", JOptionPane.INFORMATION_MESSAGE);
                cls();
                table();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "There is an error : " + ex.getMessage(), "SQL Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Updated data Canceled!!! ", "SQL Information", JOptionPane.INFORMATION_MESSAGE);
            cls();
        }
    }//GEN-LAST:event_btn_editActionPerformed

