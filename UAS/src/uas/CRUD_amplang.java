    private void table(){
        DefaultTableModel tbl  = (DefaultTableModel) tabel_amplang.getModel();
        tabel_amplang.setRowHeight(40);
        tabel_amplang.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        

//        data_anime.getColumnModel().removeColumn(data_anime.getColumnModel().getColumn(6));
//        data_anime.getColumnModel().removeColumn(data_anime.getColumnModel().getColumn(6));
        
        tbl.setRowCount(0);        
        try{
            Connection conn = Koneksi.getConnection();
            // membuat statement dan mengirimkan query ke database
            String sql = "SELECT * FROM tb_amplangs";
            PreparedStatement statement = conn.prepareStatement(sql);
            ResultSet rs = statement.executeQuery();
            // mengisi data dari ResultSet ke dalam model
            while(rs.next()) {
                int id = rs.getInt("id");
                String nama = rs.getString("nama");
                int harga = rs.getInt("harga");
                String jenis = rs.getString("jenis");
                String level = rs.getString("level");
                String foto = rs.getString("foto");
                tbl.addRow(new Object []{id, nama, harga, jenis, level, foto});
                tabel_amplang.setModel(tbl);
            }
            conn.close();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(this, "There is an error : " + e.getMessage(), "SQL Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }
    
